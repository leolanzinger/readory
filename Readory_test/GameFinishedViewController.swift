    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return self.n_players
    }
    
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("scoreCell", forIndexPath: indexPath) as! ScoreCellView
        let plImage = UIImage(named: image_array[indexPath.row])
        let plscore = score[indexPath.row + 1]
        
        cell.imageView!.image = plImage
        cell.playerScore.text = String(plscore)
        
        return cell
    }
    
    override func tableView(tableView: UITableView, viewForHeaderInSection section: Int) -> UIView? {
        let  headerCell = tableView.dequeueReusableCellWithIdentifier("headerCell")
        return headerCell
